#include "libftprintf.h"

int	ft_print_2(int nb)
{
	int		i;
	char	*s;

	i = 0;
	s = "0123456789ABCDEF";
	if (nb >= 16)
		i += ft_print_2(nb / 16);
	i += ft_print_c(s[nb % 16]);
	return (i);
}
