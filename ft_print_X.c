#include "libftprintf.h"

int	ft_print_2(int nb)
{
	int		i;
	char	s[16];

	i = 0;
	s[16] = "0123456789abcdef";
	if (nb == 0)
	{
		write(1, "0", 1);
		return (1);
	}
	if (nb >= 16)
		i += ft_print_2(nb / 16);
	if ((nb % 16) >= 'A' && (nb % 16) <= 'Z')
		write(1, &s[(nb % 16) + 32], 1);
	else
		write(1, &s[nb % 16], 1);
	i++;
	return (i);
}
