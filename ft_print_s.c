#include "ft_printf.h"

int	ft_print_s(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		ft_print_s("(null)");
		return (6);
	}
	while (s[i])
	{
		ft_print_c(s[i]);
		i++;
	}
	return (i);
}
